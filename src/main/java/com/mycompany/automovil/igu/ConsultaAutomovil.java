    private void cargarTabla() {
        //tabla no editable
        DefaultTableModel  modeloTabla = new  DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
         
        //titulo a las columnas 
        String titutlos[] = {
            "Id", "Modelo", "Marca", "Motor", "Color", "Patente", "Puertas"
        };
        modeloTabla.setColumnIdentifiers(titutlos);  
        
        //traer los autos de la bd
        List <Automovil> listaAutomoviles = control.traerAutos();
        
        tablaAutos.setModel(modeloTabla);
        
        if(listaAutomoviles != null){
            for(Automovil auto : listaAutomoviles){
                Object[] object = {auto.getId(), auto.getModelo(), auto.getMarca(), 
                    auto.getMotor(), auto.getColor(), auto.getPlaca(), auto.getCantPuertas()};
                
                modeloTabla.addRow(object);
            }
        }
        
    }
