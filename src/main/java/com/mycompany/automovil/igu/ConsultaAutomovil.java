    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // que la tabla no este vacia
        if(tablaAutos.getRowCount() > 0){
            // validar que se seleccione un registro
            if(tablaAutos.getSelectedRow() != -1){
                // obtener id del auto a borrar
                int idAuto = Integer.parseInt(String.valueOf
        (tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                
                control.borrarAuto(idAuto);
                mostrarMensaje("Auto borrado correctamente",
                        "Info", "Borrado exitoso");
                cargarTabla();
                
            } else {
                mostrarMensaje("No selecciono un registro para eliminar", 
                        "Error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia",
                    "Error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaAutos.getRowCount() > 0){
            // validar que se seleccione un registro
            if(tablaAutos.getSelectedRow() != -1){
                //obtener id para modificar
                int idAuto = Integer.parseInt(String.valueOf
        (tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));               
            
                //para modificar el auto
                ModifAuto modif = new ModifAuto(idAuto);
                modif.setVisible(true);
                modif.setLocationRelativeTo(null);
                this.dispose();
                
            } else {
                mostrarMensaje("No selecciono un registro para modificar", 
                        "Error", "Error al modificar");
            }
        } else {
            mostrarMensaje("La tabla esta vacia",
                    "Error", "Error al modificar");
        }
            
    }//GEN-LAST:event_btnEditarActionPerformed

     public void mostrarMensaje(String mensaje, String tipoMensaje, String titulo){
        
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipoMensaje.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipoMensaje.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }  
    
